const array = [1, 2, 3]; // 배열은 이터러블 프로토콜을 준수한 이터러블이다.
const iterator = array[Symbol.iterator](); // Symbol.iterator 메소드는 이터레이터를 반환한다.

// 이터레이터 프로토콜을 준수한 이터레이터는 next 메소드를 갖는다.
console.log('next' in iterator); // true

